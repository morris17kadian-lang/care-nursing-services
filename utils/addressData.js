export const getAddressSuggestions = (input) => {
  if (!input || input.length < 2) return [];
  
  const searchTerm = input.toLowerCase().trim();
  const suggestions = [];
  
  // Search in common areas (highest priority)
  const areaSuggestions = JAMAICA_LOCATIONS.commonAreas.filter(area =>
    area.toLowerCase().includes(searchTerm)
  ).slice(0, 8);
  
  // Search in streets
  const streetSuggestions = JAMAICA_LOCATIONS.streets.filter(street =>
    street.toLowerCase().includes(searchTerm)
  ).slice(0, 5);
  
  // Search in parishes
  const parishSuggestions = JAMAICA_LOCATIONS.parishes
    .filter(parish => parish.toLowerCase().includes(searchTerm))
    .map(parish => `${parish} Parish`)
    .slice(0, 3);
  
  // Combine all suggestions with priority order
  suggestions.push(...areaSuggestions);
  suggestions.push(...streetSuggestions);
  suggestions.push(...parishSuggestions);
  
  // Remove duplicates and limit to 10 suggestions
  const uniqueSuggestions = [...new Set(suggestions)];
  
  // Sort by relevance (exact matches first, then starts with, then contains)
  return uniqueSuggestions
    .sort((a, b) => {
      const aLower = a.toLowerCase();
      const bLower = b.toLowerCase();
      
      // Exact match
      if (aLower === searchTerm) return -1;
      if (bLower === searchTerm) return 1;
      
      // Starts with search term
      if (aLower.startsWith(searchTerm) && !bLower.startsWith(searchTerm)) return -1;
      if (bLower.startsWith(searchTerm) && !aLower.startsWith(searchTerm)) return 1;
      
      // Alphabetical for remaining
      return a.localeCompare(b);
    })
    .slice(0, 10);
};
